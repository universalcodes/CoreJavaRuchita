package operators;

public class RelationalOperatorss {
	
	public static void main(String[] args) {
		
		System.out.println(" <, >, <=, >=, !=, ==");
		
		int a = 45;
		int b = 41;   // Unary Operators Binary Operators , Thernary Operators
		b= 45;
		
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a!=b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a==b);  // when both values are matching together
		System.out.println(a=b);


		


	}

}
