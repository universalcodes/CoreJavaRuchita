package arrays_java;

public class TwoDimensionalArray {
	
	public static void main(String[] args) {
		
		int [][][] twod = new int [2][2][5];
		
		twod[0][0][0]= 34;
		twod[0][0][1]= 34;
		twod[0][1][0]= 34;
		twod[0][1][1]= 34;
		twod[1][0][0]= 34;
		twod[1][0][1]= 34;
		twod[1][1][1]= 34;
		twod[1][1][0]= 34;
		twod[1][1][1]= 34;




	
		System.out.println(twod[1][1][1]);

		
		
	}

}
